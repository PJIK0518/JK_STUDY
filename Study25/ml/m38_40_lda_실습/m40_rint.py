# rint = round + int : 반올림 후에 정수화 시키는 numpy 기능
import numpy as np

x = np.array([[15.2], [38.7], [60.3], [82.8], [99.9]])

x = np.rint(x)            # 부동소수점 데이터
# print(x)
# [[ 15.]
#  [ 39.]
#  [ 60.]
#  [ 83.]
#  [100.]]

x = np.rint(x).astype(int) # 정수형 데이터 : 범주형 데이터로 만들려면 정수화!!!!
# print(x)                                > 예측값은 다시 부동소수점 형태로 나오게 변화
# [[ 15]
#  [ 39]
#  [ 60]
#  [ 83]
#  [100]]